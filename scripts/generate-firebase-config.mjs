import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const envPath = join(root, ".env");
const outPath = join(root, "Public", "Script", "firebase-config.js");

function loadEnvFile(path) {
  const vars = {};
  let raw;
  try {
    raw = readFileSync(path, "utf8");
  } catch {
    return vars;
  }
  for (const line of raw.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq).trim();
    let value = trimmed.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    vars[key] = value;
  }
  return vars;
}

function pick(env, key) {
  const fromProcess = process.env[key];
  if (fromProcess != null && fromProcess !== "") return fromProcess;
  const fromFile = env[key];
  if (fromFile != null && fromFile !== "") return fromFile;
  return "";
}

const fileEnv = loadEnvFile(envPath);

const keys = [
  "VITE_FIREBASE_API_KEY",
  "VITE_FIREBASE_AUTH_DOMAIN",
  "VITE_FIREBASE_DATABASE_URL",
  "VITE_FIREBASE_PROJECT_ID",
  "VITE_FIREBASE_STORAGE_BUCKET",
  "VITE_FIREBASE_MESSAGING_SENDER_ID",
  "VITE_FIREBASE_APP_ID",
  "VITE_FIREBASE_MEASUREMENT_ID",
];

const missing = keys.filter((k) => !pick(fileEnv, k));
if (missing.length) {
  console.error(
    "Missing Firebase env vars:\n" +
      missing.map((k) => `  - ${k}`).join("\n") +
      "\n\nCopy .env.example to .env or set them in your hosting provider."
  );
  process.exit(1);
}

const config = {
  apiKey: pick(fileEnv, "VITE_FIREBASE_API_KEY"),
  authDomain: pick(fileEnv, "VITE_FIREBASE_AUTH_DOMAIN"),
  databaseURL: pick(fileEnv, "VITE_FIREBASE_DATABASE_URL"),
  projectId: pick(fileEnv, "VITE_FIREBASE_PROJECT_ID"),
  storageBucket: pick(fileEnv, "VITE_FIREBASE_STORAGE_BUCKET"),
  messagingSenderId: pick(fileEnv, "VITE_FIREBASE_MESSAGING_SENDER_ID"),
  appId: pick(fileEnv, "VITE_FIREBASE_APP_ID"),
  measurementId: pick(fileEnv, "VITE_FIREBASE_MEASUREMENT_ID"),
};

const body = `// Auto-generated by scripts/generate-firebase-config.mjs — do not edit manually.
export const firebaseConfig = ${JSON.stringify(config, null, 2)};
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, body, "utf8");
console.log("Wrote Public/Script/firebase-config.js");
